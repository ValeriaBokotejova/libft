#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}
/*
Frees the content of a single list node using the given function
and then frees the node itself, without affecting the next node.
*/
